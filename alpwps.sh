echo "                 ALP WEB SİTE IP ADRESS AND PORT SCANNER  "
echo "--------------------------------------------------------------"
# Web sitesinin IP adresini bul
echo -n "Web Site Adress: "
read website

ip_address=$(ping -c 1 $website | grep -Eo '([0-9]{1,3}\.){3}[0-9]{1,3}' | tail -1)

if [ -z "$ip_address" ]; then
    echo "IP adress not found."
    exit 1
fi

echo "Web site IP adress: $ip_address"

# Portları tara ve sonucu dosyaya kaydet
echo "Scanning for open ports..."
for port in {1..65535}; do
    (echo >/dev/tcp/$ip_address/$port) >/dev/null 2>&1 && echo $port >> open_ports.txt
done

echo "Open Ports:"
cat open_ports.txt

echo "Open Ports 'open_ports.txt' saved."
